    [MenuItem("Tools/Matrix Cube Generator")]
    public static void ShowWindow() {
        GetWindow<MatrixCubesWindow>("Matrix Cube Generator");
    }

    private void OnGUI() {
        GUILayout.Label("Configuración de la Matriz", EditorStyles.boldLabel);
        cubePrefab = (GameObject)EditorGUILayout.ObjectField("Prefab de Cubo", cubePrefab, typeof(GameObject), false);
        startPosition = EditorGUILayout.Vector2IntField("Posición Inicial", startPosition);
        spacing = EditorGUILayout.FloatField("Espaciado", spacing);

        if(GUILayout.Button("Generar Cubos")) {
            GenerateCubes();
        }

        if(GUILayout.Button("Eliminar Cubos")) {
            ClearCubes();
        }
    }

    private void GenerateCubes() {
        if(cubePrefab == null) {
            Debug.LogError("¡Asigna un prefab de cubo antes de generar!");
            return;
        }

        GameObject parent = new GameObject("GeneratedCubes");

        int rows = matrix.GetLength(0);
        int cols = matrix.GetLength(1);

        for(int i = 0; i < rows; i++) {
            for(int j = 0; j < cols; j++) {
                if(matrix[i, j] == 0) {
                    Vector3 position = new Vector3(startPosition.x + j * spacing, 0, startPosition.y - i * spacing);
                    Instantiate(cubePrefab, position, Quaternion.identity, parent.transform);
                }
            }
        }
    }

    private void ClearCubes() {
        GameObject parent = GameObject.Find("GeneratedCubes");
        if(parent != null) {
            DestroyImmediate(parent);
        }
    }
